/**
 * I2C 写命令到 OLED
 */
static void OledWriteCmd(unsigned char cmd)
{
    unsigned char buf[2] = { 0x00, cmd };  /* 0x00 = 命令前缀 */
    IoTI2cWrite(OLED_I2C_IDX, OLED_ADDR, buf, 2);
}

/**
 * I2C 写数据到 OLED
 */
static void OledWriteData(unsigned char data)
{
    unsigned char buf[2] = { 0x40, data };  /* 0x40 = 数据前缀 */
    IoTI2cWrite(OLED_I2C_IDX, OLED_ADDR, buf, 2);
}

void OledInit(void)
{
    usleep(100000);  /* 等待 OLED 上电稳定 */

    OledWriteCmd(0xAE); /* 关闭显示 */
    OledWriteCmd(0x20); /* 设置内存寻址模式: 水平寻址 */
    OledWriteCmd(0x00);
    OledWriteCmd(0xB0); /* 设置页起始地址 */
    OledWriteCmd(0xC8); /* COM 扫描方向 */
    OledWriteCmd(0x00); /* 低列地址 */
    OledWriteCmd(0x10); /* 高列地址 */
    OledWriteCmd(0x40); /* 起始行 */
    OledWriteCmd(0x81); /* 对比度设置 */
    OledWriteCmd(0xFF);
    OledWriteCmd(0xA1); /* 段重映射 */
    OledWriteCmd(0xA6); /* 正常显示 (非反色) */
    OledWriteCmd(0xA8); /* 设置复用率 */
    OledWriteCmd(0x3F); /* 64行 */
    OledWriteCmd(0xA4); /* 全局显示开 */
    OledWriteCmd(0xD3); /* 显示偏移 */
    OledWriteCmd(0x00);
    OledWriteCmd(0xD5); /* 时钟分频 */
    OledWriteCmd(0xF0);
    OledWriteCmd(0xD9); /* 预充电周期 */
    OledWriteCmd(0x22);
    OledWriteCmd(0xDA); /* COM 引脚配置 */
    OledWriteCmd(0x12);
    OledWriteCmd(0xDB); /* VCOMH 电压 */
    OledWriteCmd(0x20);
    OledWriteCmd(0x8D); /* 电荷泵 */
    OledWriteCmd(0x14); /* 开启电荷泵 */
    OledWriteCmd(0xAF); /* 开启显示 */

    printf("[OLED] SSD1306 initialized\r\n");
}

void OledFillScreen(unsigned char fillData)
{
    unsigned char page, col;
    for (page = 0; page < 8; page++) {
        OledWriteCmd(0xB0 + page);  /* 设置页地址 */
        OledWriteCmd(0x00);          /* 低列地址 */
        OledWriteCmd(0x10);          /* 高列地址 */
        for (col = 0; col < OLED_WIDTH; col++) {
            OledWriteData(fillData);
        }
    }
}

/**
 * 在指定位置显示字符串
 * @param x: 列 (0-21, 每字符宽6像素)
 * @param y: 行 (0-7, 每行高8像素)
 * @param string: 要显示的字符串
 */
void OledShowString(unsigned char x, unsigned char y, const char *string)
{
    unsigned char c, i;

    /* 设置页和列地址 */
    OledWriteCmd(0xB0 + y);
    OledWriteCmd(0x00 + (x * 6 & 0x0F));
    OledWriteCmd(0x10 + ((x * 6 >> 4) & 0x0F));

    while (*string) {
        c = *string - ' ';  /* 字体从空格开始 */

        /* 边界检查 */
        if (c > 58) c = 0;  /* 超范围显示空格 */

        for (i = 0; i < 6; i++) {
            OledWriteData(g_font6x8[c][i]);
        }
        string++;
    }
}
